func TestRiskFlags_DocsOnly(t *testing.T) {
	files := []ChangedFile{
		{Path: "README.md"},
		{Path: "docs/guide.mdx"},
	}
	got := flagNames(RiskFlags(files))
	if _, ok := got["docs_only"]; !ok {
		t.Error("expected docs_only to be raised for a docs-only diff")
	}
	if _, ok := got["tests_missing_for_code_change"]; ok {
		t.Error("docs-only diff must not raise tests_missing_for_code_change")
	}
}

func TestRiskFlags_CodeWithTestsDoesNotFlagMissingTests(t *testing.T) {
	files := []ChangedFile{
		{Path: "packages/core/api/client.ts"},
		{Path: "packages/core/api/client.test.ts"},
	}
	got := flagNames(RiskFlags(files))
	if _, ok := got["tests_missing_for_code_change"]; ok {
		t.Error("code change accompanied by a test must not raise tests_missing_for_code_change")
	}
}

func TestRiskFlags_LargeDiff(t *testing.T) {
	files := make([]ChangedFile, largeDiffThreshold)
	for i := range files {
		files[i] = ChangedFile{Path: "docs/file" + string(rune('a'+i%26)) + ".md"}
	}
	got := flagNames(RiskFlags(files))
	if _, ok := got["large_diff"]; !ok {
		t.Errorf("expected large_diff to be raised at %d files", largeDiffThreshold)
	}
}

func TestBuildReport_Shape(t *testing.T) {
	files := []ChangedFile{
		{Path: "server/internal/middleware/auth.go"},
		{Path: "CONTRIBUTING.md"},
	}
	r := BuildReport("origin/main", "HEAD", files)
	if r.BaseRef != "origin/main" || r.HeadRef != "HEAD" {
		t.Errorf("refs not preserved: %+v", r)
	}
	if len(r.ChangedFiles) != 2 {
		t.Errorf("changedFiles = %v, want 2", r.ChangedFiles)
	}
	if _, ok := r.Subsystems["security_auth"]; !ok {
		t.Error("expected security_auth subsystem")
	}
	if _, ok := r.Subsystems["docs"]; !ok {
		t.Error("expected docs subsystem")
	}
	if r.Summary == "" {
		t.Error("summary should not be empty")
	}
}
