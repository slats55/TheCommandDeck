func TestClassifyPath(t *testing.T) {
	cases := []struct {
		path string
		want Subsystem
	}{
		// docs-only
		{"CONTRIBUTING.md", SubsystemDocs},
		{"apps/docs/content/docs/developers/contributing.zh.mdx", SubsystemDocs},
		{"docs/commanddeck/runbooks/LOCAL-DOGFOOD-ACCESS.md", SubsystemDocs},
		// migration
		{"server/migrations/091_add_widget.up.sql", SubsystemDatabase},
		{"server/migrations/084_command_template.down.sql", SubsystemDatabase},
		// auth / security
		{"server/internal/middleware/auth.go", SubsystemSecurityAuth},
		{"server/internal/middleware/daemon_auth.go", SubsystemSecurityAuth},
		{"packages/core/platform/auth-initializer.tsx", SubsystemSecurityAuth},
		{"apps/web/app/(auth)/login/page.tsx", SubsystemSecurityAuth},
		// command runner / execution
		{"server/internal/handler/commandrunner.go", SubsystemCommandExec},
		{"server/internal/handler/commandworkflow.go", SubsystemCommandExec},
		// runtime / daemon
		{"server/internal/daemon/manager.go", SubsystemDaemonRuntime},
		{"server/internal/handler/runtime_liveness_store.go", SubsystemDaemonRuntime},
		// infra / docker
		{"compose.yml", SubsystemInfraDocker},
		{"docker-compose.yml", SubsystemInfraDocker},
		{"apps/web/Dockerfile", SubsystemInfraDocker},
		// ci
		{".github/workflows/ci.yml", SubsystemGithubCI},
		// scripts / devtools
		{"scripts/ensure-postgres.sh", SubsystemScriptsDevtools},
		{"Makefile", SubsystemScriptsDevtools},
		// tests
		{"server/internal/handler/config_test.go", SubsystemTests},
		{"packages/core/api/client.test.ts", SubsystemTests},
		{"e2e/tests/login.spec.ts", SubsystemTests},
		// backend / frontend fallthrough
		{"server/internal/handler/config.go", SubsystemBackend},
		{"packages/core/api/client.ts", SubsystemFrontend},
		{"apps/web/app/page.tsx", SubsystemFrontend},
		// unknown
		{"weird-root-file.xyz", SubsystemUnknown},
		{"author/notes", SubsystemUnknown}, // "author" must NOT match the auth token
	}
	for _, c := range cases {
		if got := ClassifyPath(c.path); got != c.want {
			t.Errorf("ClassifyPath(%q) = %q, want %q", c.path, got, c.want)
		}
	}
}

func flagNames(flags []RiskFlag) map[string]RiskFlag {
	m := map[string]RiskFlag{}
	for _, f := range flags {
		m[f.Name] = f
	}
	return m
}

func TestRiskFlags_MigrationAndAuth(t *testing.T) {
	files := []ChangedFile{
		{Path: "server/migrations/091_widget.up.sql"},
		{Path: "server/internal/middleware/auth.go"},
		{Path: "server/internal/handler/commandrunner.go"},
		{Path: "server/internal/daemon/manager.go"},
	}
	got := flagNames(RiskFlags(files))
	for _, name := range []string{
		"migrations_touched", "auth_security_touched",
		"command_execution_touched", "runtime_daemon_touched",
		"tests_missing_for_code_change",
	} {
		if _, ok := got[name]; !ok {
			t.Errorf("expected risk flag %q to be raised", name)
		}
	}
	if got["migrations_touched"].Severity != "high" {
		t.Errorf("migrations_touched severity = %q, want high", got["migrations_touched"].Severity)
	}
}

func TestRiskFlags_DockerComposeByContent(t *testing.T) {
	// A devtools file that is not itself a compose file but wields it should
	// still raise docker_compose_touched via content scan (matches the
	// Makefile / ensure-postgres.sh case from the compose-precedence fix).
	files := []ChangedFile{
		{Path: "scripts/ensure-postgres.sh", Content: "docker compose -f docker-compose.yml up -d postgres"},
		{Path: "Makefile", Content: "COMPOSE := docker compose -f docker-compose.yml"},
	}
	got := flagNames(RiskFlags(files))
	flag, ok := got["docker_compose_touched"]
	if !ok {
		t.Fatal("expected docker_compose_touched to be raised by content scan")
	}
	if len(flag.Evidence) != 2 {
		t.Errorf("docker_compose_touched evidence = %v, want 2 files", flag.Evidence)
	}
}

